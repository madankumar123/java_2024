package com.practice;

public class chp_22_DoWhileLoop {
    public static void main(String[] args) {
        int a = 45;                           // if a =0 then the  o/p: 0 to 50
        do {
            System.out.println(a);
            a++;
        } while (a<=50);            // o/p: 45,46,47,48,49,50

    }
}
